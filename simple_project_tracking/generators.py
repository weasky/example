import itertools

def dict_gen(curs):
    ''' From David Beazley's Python Essential Reference.
    
        Memory-sipping transformation of db results from 
        tuples to dicts.
    '''
    field_names = [d[0].lower() for d in curs.description]
    while True:
        rows = curs.fetchmany()
        if not rows: return
        for row in rows:
            yield dict(itertools.izip(field_names, row))
